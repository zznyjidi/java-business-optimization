public class Employee{
    String name;
    String jobtitle;
    int speed;
    int coverage;
    int fulfilment;
    String salary;

    public Employee(String name, String jobtitle, int speed, int coverage, int fulfilment, String salary){
        this.name = name;
        this.jobtitle = jobtitle;
        this.speed = speed;
        this.coverage = coverage;
        this.fulfilment = fulfilment;
        this.salary = salary;
    }
}